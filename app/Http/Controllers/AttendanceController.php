     */
    public function store(Request $request)
    {
        $attendance= new Attendance;
        $attendance->user_id=$request->get('ban_id');
        $attendance->in_time=$request->get('in_time');
        $attendance->out_time=$request->get('out_time');
        $attendance->date =$request->get('date');
        $attendance->save();
        return back()->with('success', 'Information has been added');
    }

